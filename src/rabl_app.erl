-module(rabl_app).

-behaviour(application).

-export([start/0]).
%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start() ->
    ok = amqp_client:start(),
    application:start(rabl).

start(_StartType, _StartArgs) ->
    lager:info("rabl starting"),
    rabl_sup:start_link().


%%--------------------------------------------------------------------
stop(_State) ->
    ok.

